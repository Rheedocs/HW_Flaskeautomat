package util;

// ANSI farvekoder til konsoloutput, for klar læsbarhed.
public class AnsiColors {
    public static final String RESET   = "\u001B[0m";
    public static final String GREEN   = "\u001B[32m";  // Producer
    public static final String YELLOW  = "\u001B[33m";  // Splitter
    public static final String BLUE    = "\u001B[34m";  // ØL-consumer
    public static final String MAGENTA = "\u001B[35m";  // VAND-consumer
}